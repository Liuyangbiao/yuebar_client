public class RechargRecordsModel {
	private String recordsContent;// 记录详情
	private String recordsDate;// 记录日期

	public RechargRecordsModel(String recordsContent, String recordsDate) {
		this.recordsContent = recordsContent;
		this.recordsDate = recordsDate;
	}

	public RechargRecordsModel() {
	}

	public String getRecordsContent() {
		return recordsContent;
	}

	public void setRecordsContent(String recordsContent) {
		this.recordsContent = recordsContent;
	}

	public String getRecordsDate() {
		return recordsDate;
	}

	public void setRecordsDate(String recordsDate) {
		this.recordsDate = recordsDate;
	}

}
