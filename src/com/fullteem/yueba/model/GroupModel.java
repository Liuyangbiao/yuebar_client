package com.fullteem.yueba.model;

import java.io.Serializable;

import com.fullteem.yueba.widget.CircleImageView;

/**
 * 附近群组model
 * 
 * @author ssy
 */
public class GroupModel implements Serializable {

	/**
	 * 
	 */
	private static final long serialVersionUID = 1L;

	/**
	 * 头像
	 */
	private CircleImageView headerImg;

	/**
	 * 群组名
	 */
	private String groupName;

	/**
	 * 人数
	 */
	private String peopleNums;

	/**
	 * 详细
	 */
	private String details;

	/**
	 * 距离
	 */
	private String space;

	public String getGroupName() {
		return groupName;
	}

	public void setGroupName(String groupName) {
		this.groupName = groupName;
	}

	public String getPeopleNums() {
		return peopleNums;
	}

	public void setPeopleNums(String peopleNums) {
		this.peopleNums = peopleNums;
	}

	public String getDetails() {
		return details;
	}

	public void setDetails(String details) {
		this.details = details;
	}

	public String getSpace() {
		return space;
	}

	public void setSpace(String space) {
		this.space = space;
	}

	public CircleImageView getHeaderImg() {
		return headerImg;
	}

	public void setHeaderImg(CircleImageView headerImg) {
		this.headerImg = headerImg;
	}

}