package com.fullteem.yueba.net.http;

/**
 * 如果想对http返回的数据进行处理，需要继承此接口，接口包含了成功，失败，timeout三种返回的方法
 * 
 * @author ssy
 * 
 */
public interface IHttpTaskCallBack {

	public void TaskSuccess(String result, int flag);

	public void TaskFaild(String result);

	public void TimeOut(String result);
}
